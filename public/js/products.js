function renderSizes(containerId, name, sizes = product.sizes) {
  const container = document.getElementById(containerId);
  if (!container) return;

  container.innerHTML = sizes
    .map(
      (size, i) => `
      <label class="variant-btn">
        <input type="radio" name="${name}" value="${size}" ${
        i === 0 ? "checked" : ""
      } hidden>
        ${size}
      </label>
    `
    )
    .join("");
}

