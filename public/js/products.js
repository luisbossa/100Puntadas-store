function renderColors(containerId, name, colors) {
  const container = document.getElementById(containerId);
  if (!container) return;

  container.innerHTML = colors
    .map(
      (color, i) => `
      <label class="color-swatch">
        <input 
          type="radio" 
          name="${name}" 
          value="${color.id}" 
          ${i === 0 ? "checked" : ""} 
          hidden
        >
        <span class="color-circle" data-color="${color.id}"></span>
      </label>
    `
    )
    .join("");
}

